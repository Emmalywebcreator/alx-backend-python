#!/usr/bin/env python3
"""
This module provides a function that concatenate two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    A type-annotated  function that takes two strings as an
    argument and return a concatenated string


    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
