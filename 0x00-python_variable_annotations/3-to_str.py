#!/usr/bin/env python3
"""
This module provides a function that converts a float to its string representation.
"""


def to_str(n: float) -> str:
    """
    A type-annotated function that takes a float as an argument
    and returns the string representation of the float.

    Args:
        n (float): The float number to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
